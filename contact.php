			Once upon a time, during the times of Mughal Era, a karigar pickeup up a needle and went on to stitch the hum in his heart. From that time up until now, Chaikankari embroidery has thirty six different stitching techniques that in today's times are often combined with embellishments of pearls, mirror, katdana, aari work and mukaish.The name 'Chikan' has been derived from the Persian word 'Chikan' or 'Chikeen', meaning a kind of cloth wrought with needle work.
			<br><br>
			Known as the most ancient decoration style,nearly two hundred years old, Chikankari traces it's origins as a court craft during the reign of Mughal Emperor Jahangir and was introduced by his wife Noor Jahan. It later enjoyed the patronage of the Nawabs of Awadh (Lucknow).
			<br><br>                                                                                                        
			'Chikan Sazi' is the exquisite needle work of fine and delicate floral patterns stitched using untwisted cotton and silk threads. The patterns are mostly inspired by Mughal architecture and the cloth used can be cotton, muslin, chiffon, georgette, organza, chanderi, etc.
			 <br><br>                                                                                                                                                    
			Handmade Chikankari apparels are so much different than the machine-made clones of theirs. They aren’t made in a day, the making of one apparel takes several days. Handmade embroidery allows for a variety of stitches, thread and fabric. Every work is an art-piece; unique in its own way. Even if an apparel is duplicated with the same pattern, it is the needle artist’s discretion that makes each piece a one of a kind work of art.It’s what makes hand embrodiered clothing so sought after.
			<br><br>
